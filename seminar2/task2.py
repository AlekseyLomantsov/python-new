# 2. Для натурального n создать последовательности 3n + 1.
    
#     *Пример:*
#     - Для n = 6: 
#         1: 4, 
#         2: 7, 
#         3: 10, 
#         4: 13, 
#         5: 16, 
#         6: 19

n = int(input('Ввдите число: '))
count = 0
while count < n:
    print(f'{count + 1}: {(count + 1) * 3 + 1}')
    count += 1